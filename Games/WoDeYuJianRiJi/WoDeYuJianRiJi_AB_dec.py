def decrypt(path):
    data = bytearray(Path(path).read_bytes())

    if data[:2] != b"AB":
        return

    v5 = ((data[13] | (int.from_bytes(data[2:4], "big") << 16))
          & 0xffff00ff) | (data[12] << 8)

    v6 = int.from_bytes(data[14:16], "big")
    v7 = int.from_bytes(data[4:6] + data[10:12], "big")

    size = min(v7 % v6 + v7, len(data))

    if v5 + size + 16 >= len(data):
        return

    idx = v5 % 3 if (size & 3) else 3
    key = KEYS[idx]

    start = v5 + 16

    for i in range(size):
        data[start + i] ^= key[(i + v5) % 33]

    Path(path).write_bytes(data[16:])


